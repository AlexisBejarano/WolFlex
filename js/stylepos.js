$(document).ready(function(){

	$('.miswitch1 a').click(function(){
		$('.pricing-table-month1').toggleClass('on');

		if($('#swicht-btn').attr('class') == 'swicht-btn on'){
			$('.pricing-table-cont1').toggleClass('rotando-tabla');
		} else{
			$('.pricing-table-cont1').toggleClass('rotando-tabla');
		}

	});


});

